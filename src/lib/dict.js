export const locales = ['en', 'ja'];

export const providers = {
	"google":"Google ID",
	"discord":"Discord ID",
	"github":"GitHub ID",
};


export function addDict( t){
	for( let [i,v] of t.entries()){ dict[i] = v; }
}

function replaces( s, reps){
	if( typeof s == "object"){
		return s.map( (v,i) => replaces( v, reps));
	}
	if( reps){
		for( let [i,v] of Object.entries( reps)) if( v){ s = s.replace( i, v); }
	}
	return s;
}

export function getDict( lang){
	return ( id, reps=null) => {
		let d = dict[id];
		if( d){
			let v = d[lang];
			if( v) return replaces( v, reps);
			for( let i of locales){
				if( (v=d[i])) return replaces( v, reps);
			}
		}
		return id;
	}
}
